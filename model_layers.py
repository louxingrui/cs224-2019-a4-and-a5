        encoder_outputs = super().forward(
            input_ids=input_ids,
            attention_mask=attention_mask,
            encoder_hidden_states=encoder_hidden_states,
            encoder_attention_mask=encoder_attention_mask,
            inputs_embeds=inputs_embeds,
            head_mask=head_mask,
            encoder_head_mask=encoder_head_mask,
            past_key_values=past_key_values,
            use_cache=use_cache,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
        )
        hidden_states = encoder_outputs.last_hidden_state   # (batch_size, seq_len, hidden_size)
        # added code here
        emotion_representation = hidden_states[:, 0, :]
        # TODO:resize attention_mask
        # print(attention_mask.shape)
        content_representations, _ = self.averageSelfAttention(hidden_states[:, 1:, :],
                                                               attention_mask[:, 1:].squeeze(1).squeeze(1))
        c_mean = self.c_mean(content_representations)
        c_logvar = self.c_logvar(content_representations)
        e_mean = self.e_mean(emotion_representation)
        e_logvar = self.e_logvar(emotion_representation)

        return c_mean, c_logvar, e_mean, e_logvar, encoder_outputs

